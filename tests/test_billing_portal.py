import os
import sys
import uuid
from fastapi.testclient import TestClient

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))
os.environ.setdefault("OPENAI_API_KEY", "test")
os.environ.setdefault("DATABASE_URL", "sqlite:///:memory:")

from main import app
from auth.auth_utils import create_access_token

client = TestClient(app)


def register_and_login():
    """Register a user and return id plus auth token."""
    email = f"portal_{uuid.uuid4().hex}@example.com"
    user_data = {
        "email": email,
        "phone_number": str(int(uuid.uuid4().int % 10_000_000_000)).zfill(10),
        "hashed_password": "password123",
    }
    resp = client.post("/users/", json=user_data)
    assert resp.status_code in (200, 201)
    user_id = resp.json()["id"]
    token = create_access_token({"user_id": user_id})
    return user_id, token


def test_billing_portal_endpoint():
    """Endpoint should provide a portal URL for the user."""
    _, token = register_and_login()
    headers = {"Authorization": f"Bearer {token}"}
    resp = client.get("/billing/portal", headers=headers)
    assert resp.status_code == 200
    data = resp.json()
    assert "url" in data

